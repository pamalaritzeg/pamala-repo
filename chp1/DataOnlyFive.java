class DataOnlyFive {
	  int i;
	  double d;
	  boolean b;
	  
	  public static void main(String[] args) {
	  DataOnlyFive data = new DataOnlyFive();
	  data.i = 47;
	  data.d = 1.1;
	  data.b = false;
	  System.out.println("Data Values: ");
	  System.out.println(data.i);
	  System.out.println(data.d);
	  System.out.println(data.b);
	  }
}
